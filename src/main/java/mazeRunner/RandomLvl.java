    public static String[][] drawRandom() {

        String[][] randomArray = new String[20][20];
        for (int i = 0; i < 20; i++) System.arraycopy(array[i], 0, randomArray[i], 0, 20);

        int r;
        Random random = new Random();
        r = random.nextInt(15) + 1;
        if (r > 2) {
            for (int i = 0; i < 6; i++) {

                switch (i) {
                    case 0, 1 -> r = random.nextInt(4) + 2;
                    case 2, 3 -> r = random.nextInt(6) + 7;
                    case 4, 5 -> r = random.nextInt(4) + 14;
                }

                if (i == 0 || i == 2 || i == 4) {
                    for (int j = 0; j < 18; j++) {
                        int r2 = random.nextInt(2) + 1;
                        if (j % r2 == 0) randomArray[r][j] = WALL.getName();
                    }
                } else {
                    for (int j = 2; j < 18; j++) {
                        int r2 = random.nextInt(3) + 1;
                        if (j % r2 == 0) randomArray[j][r] = WALL.getName();
                        else if (r % r2 == 0) randomArray[j + 1][r + 1] = ENEMY.getName();
                    }
                }
            }
            r = random.nextInt(15) + 4;
            randomArray[19][r] = EXIT.getName();

        } else {
            for (int i = 4; i < 15; i++) {
                randomArray[9][i] = WALL.getName();
            }
            randomArray[10][10] = BOSS.getName();
        }
        return randomArray;
    }
